const fs = require('fs');

const svg = fs.readFileSync('public/vehicle-diagrams/yaris cross outline.svg', 'utf8');

// Extract all path elements with their attributes
const pathRegex = /<path\s+d="([^"]*)"\s+fill="([^"]*)"\s+transform="translate\(([^)]*)\)"[^/]*\/>/g;
const paths = [];
let m;
while ((m = pathRegex.exec(svg)) !== null) {
  const d = m[1];
  const fill = m[2];
  const [tx, ty] = m[3].split(',').map(Number);
  if (d && d.length > 0) {
    paths.push({ d, fill, tx, ty });
  }
}

console.log(`Total paths with content: ${paths.length}`);

// Zone classification based on coordinate picker landmarks:
// Front wheel center: (355, 624), Rear wheel center: (1203, 634)
// Front door center: (682, 502), Rear door center: (987, 497)
// Hood: (277, 417), Windshield: (536, 342), Roof: (953, 255)
// Rear window: (1274, 321), Rear bodywork: (1260, 454)
// Back side: (1408, 499), Rear bumper: (1430, 598)
// Front bumper: (84, 595), Top bumper: (121, 451)

function classifyZone(tx, ty) {
  // Wheels (circles around wheel centers)
  const fwDist = Math.sqrt((tx - 355) ** 2 + (ty - 624) ** 2);
  if (fwDist < 110) return 'front_left_rim';
  const rwDist = Math.sqrt((tx - 1203) ** 2 + (ty - 634) ** 2);
  if (rwDist < 110) return 'rear_left_rim';

  // Sill / left side (between wheels, bottom strip)
  if (ty >= 590 && tx >= 460 && tx < 1100) return 'left_side';

  // Front bumper (far left)
  if (tx < 180) {
    if (ty < 370) return 'hood'; // very top-front goes to hood
    return 'front_bumper';
  }

  // Rear bumper (far right)
  if (tx >= 1400) return 'rear_bumper';

  // Roof (top strip)
  if (ty < 260 && tx >= 530 && tx < 1090) return 'roof';

  // Windshield (angled front glass area)
  if (tx >= 400 && tx < 580 && ty < 410) {
    // Check if it's in the windshield triangle
    // Line from (425,400) to (555,190) — below this line is fender, above is windshield
    const windshieldSlope = (190 - 400) / (555 - 425); // negative slope
    const windshieldY = 400 + windshieldSlope * (tx - 425);
    if (ty < windshieldY + 20) return 'windshield';
  }

  // Hood (front upper area)
  if (tx >= 180 && tx < 430 && ty < 450) {
    // Above the beltline at this x position
    const hoodBottomY = 450 - (tx - 180) * (450 - 350) / (430 - 180);
    if (ty < hoodBottomY + 30) return 'hood';
  }

  // Rear window (angled rear glass)
  if (tx >= 1080 && tx < 1320 && ty < 410) {
    const rwSlope = (385 - 185) / (1315 - 1085);
    const rwY = 185 + rwSlope * (tx - 1085);
    if (ty < rwY + 20) return 'rear_window';
  }

  // Trunk (upper rear panel)
  if (tx >= 1260 && tx < 1400 && ty >= 370 && ty < 570) return 'trunk';

  // Front left fender
  if (tx >= 180 && tx < 530) {
    if (ty >= 350) return 'front_left_fender';
    return 'hood'; // above beltline at front
  }

  // Front left door
  if (tx >= 530 && tx < 830) return 'front_left_door';

  // Rear left door
  if (tx >= 830 && tx < 1100) return 'rear_left_door';

  // Rear left fender
  if (tx >= 1100 && tx < 1400) {
    if (ty < 300) return 'rear_window';
    return 'rear_left_fender';
  }

  return 'unclassified';
}

// Classify all paths
const zones = {};
let unclassified = 0;

paths.forEach(p => {
  const zone = classifyZone(p.tx, p.ty);
  if (zone === 'unclassified') {
    unclassified++;
    return;
  }
  if (!zones[zone]) zones[zone] = [];
  zones[zone].push({ d: p.d, tx: p.tx, ty: p.ty, fill: p.fill });
});

console.log(`\nZone counts:`);
Object.keys(zones).sort().forEach(z => console.log(`  ${z}: ${zones[z].length} paths`));
console.log(`  unclassified: ${unclassified}`);

// Write output as TypeScript data file
let ts = `// Auto-generated from yaris cross outline.svg — do not edit manually\n`;
ts += `// Generated by scripts/process-svg-zones.js\n\n`;
ts += `export interface SvgZonePath {\n  d: string;\n  tx: number;\n  ty: number;\n  fill: string;\n}\n\n`;
ts += `export const svgZonePaths: Record<string, SvgZonePath[]> = {\n`;

Object.keys(zones).sort().forEach(zone => {
  ts += `  ${zone}: [\n`;
  zones[zone].forEach(p => {
    // Escape any problematic chars in d
    const escaped = p.d.replace(/\\/g, '\\\\').replace(/`/g, '\\`');
    ts += `    {d:\`${escaped}\`,tx:${p.tx},ty:${p.ty},fill:"${p.fill}"},\n`;
  });
  ts += `  ],\n`;
});

ts += `};\n`;

fs.writeFileSync('components/admin/yarisZonePaths.ts', ts);
console.log(`\nWrote components/admin/yarisZonePaths.ts (${(ts.length / 1024).toFixed(0)} KB)`);
